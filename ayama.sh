        iso_link="https://www.dropbox.com/scl/fi/6myx8gordhwgdh10kb37l/windows2016.gz?rlkey=wsbmbkhpniq8slca25jhi0ce4&st=iergt69v&dl=0"
        iso_file="windows2016.iso"
